package Java_Core_Classes.day18_Nested_Loop;

public class NestedLoopPractice2 {

    public static void main(String[] args) {

        for (int i = 0; i < 10; i++) {
            for (int j = 0; j < 20; j++) {
                System.out.print("* ");
            }
            System.out.println();
        }

















    }
}
