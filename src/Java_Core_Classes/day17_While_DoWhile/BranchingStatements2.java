package Java_Core_Classes.day17_While_DoWhile;

public class BranchingStatements2 {

    public static void main(String[] args) {

        for (char i = 'A'; i <= 'E'; i++) {

            if(i == 'C')  //to skip a character, a statement
                continue;
            System.out.println(i);

        }



    }
}
