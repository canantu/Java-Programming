package week08_10_29_2022;

public class ForEachLoop {

    public static void main(String[] args) {

        int[] numbers = {1,2,3,4,5,6};

        for (int number : numbers) {
            System.out.println(number);
        }

        for(int each : numbers){
            System.out.println(each);
        }






    }
}
